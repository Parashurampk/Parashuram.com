package JavaLoops;

public class Switch {

	public static void main(String[] args) {

		int x = 3;
		switch (x) {
		case 1:
			System.out.println("performnce is poor");
			break;

		case 2:
			System.out.println("performnce is Good");
			break;

		case 3:
			System.out.println("performnce is better");
			break;

		default:
			System.out.println("You are fired");
			break;
		}

	}

}
